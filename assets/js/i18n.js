  function getStoredLang() {
    try {
      var stored = window.localStorage.getItem(STORAGE_KEY);
      if (stored && dict[stored]) return stored;
    } catch (e) { /* localStorage unavailable (e.g. privacy mode) */ }
    return DEFAULT_LANG;
  }

  function applyLang(lang) {
    var strings = dict[lang] || dict[DEFAULT_LANG];

    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      var key = el.getAttribute("data-i18n");
      if (strings[key] !== undefined) el.innerHTML = strings[key];
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach(function (el) {
      var key = el.getAttribute("data-i18n-placeholder");
      if (strings[key] !== undefined) el.setAttribute("placeholder", strings[key]);
    });

    document.querySelectorAll("[data-i18n-aria-label]").forEach(function (el) {
      var key = el.getAttribute("data-i18n-aria-label");
      if (strings[key] !== undefined) el.setAttribute("aria-label", strings[key]);
    });

    document.querySelectorAll("[data-lang-switch] button").forEach(function (btn) {
      btn.classList.toggle("is-active", btn.getAttribute("data-lang") === lang);
    });

    document.documentElement.setAttribute("lang", lang);
  }

  function setLanguage(lang) {
    if (!dict[lang]) lang = DEFAULT_LANG;
    try { window.localStorage.setItem(STORAGE_KEY, lang); } catch (e) { /* ignore */ }
    applyLang(lang);
  }

  function currentLang() { return getStoredLang(); }

  function t(key) {
    var strings = dict[currentLang()] || dict[DEFAULT_LANG];
    return strings[key] !== undefined ? strings[key] : key;
  }

  document.addEventListener("DOMContentLoaded", function () {
    applyLang(getStoredLang());

    document.querySelectorAll("[data-lang-switch] button").forEach(function (btn) {
      btn.addEventListener("click", function () {
        setLanguage(btn.getAttribute("data-lang"));
      });
    });
  });

  window.GRODMAX_I18N = { setLanguage: setLanguage, currentLang: currentLang, t: t, dict: dict };
